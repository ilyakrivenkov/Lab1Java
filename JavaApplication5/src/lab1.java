    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // Получаем индекс выбранной строки и столбца
        int selectedRow = jTable1.getSelectedRow();
        int selectedColumn = jTable1.getSelectedColumn();
    
        // Проверяем, что выбранная ячейка не пуста
        if (selectedRow != -1 && selectedColumn != -1) {
            // Устанавливаем значение выбранной ячейки как null или пустую строку
            jTable1.setValueAt(null, selectedRow, selectedColumn);
        } else {
            // Если ячейка не выбрана, выводим сообщение об ошибке
            JOptionPane.showMessageDialog(this, "Пожалуйста, выберите ячейку для удаления значения.", "Ошибка", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

