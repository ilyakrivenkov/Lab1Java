    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = 0; // использовать первую строку таблицы

        double lowerBound = Double.parseDouble(model.getValueAt(row, 0).toString());
        double upperBound = Double.parseDouble(model.getValueAt(row, 1).toString());
        double step = Double.parseDouble(model.getValueAt(row, 2).toString());
        double i = 0;
        double result = 0;
//        if((lowerBound + step) > upperBound){
//            for (double x = lowerBound; x < upperBound; x += step) {
//                result += 1 / x * step;
//            }
//        }
//        else{
//
//        }
        for(i = lowerBound; i < upperBound; i+=step){
            if((i + step) < upperBound) {
                result += ((1/i) + (1/(step + i))) * (step/2);
            }
            else{
                result += ((1/i) + (1/upperBound)) * ((upperBound - i)/2);
            }
        }
        model.setValueAt(result, row, 3); // Устанавливаем значение результата в четвертый столбец таблицы
    }//GEN-LAST:event_jButton3ActionPerformed

